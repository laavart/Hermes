    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String username, password;
        boolean checkUsername, checkPassword;
        
        username = jTextField1.getText();
        password = jPasswordField1.getText();
        
        checkUsername = Register.checkUsename(username);
        checkPassword = Validate.matchPassword(username, password);
        
        if(checkUsername==true && checkPassword==true){
            JOptionPane.showMessageDialog(rootPane, "Correct Creditials, Welcome to System!!!");
        }
        else if(checkUsername==false){
            JOptionPane.showMessageDialog(rootPane, "User not Found, Try Again!!!");
            jTextField1.setText("");
            jPasswordField1.setText("");
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Incorrect Password, Enter Correct Password!!!");
            jPasswordField1.setText("");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

