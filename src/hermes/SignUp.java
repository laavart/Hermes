    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String username, password, confirm;
        int securityCode;
        boolean checkUsername, checkPassword, checkSecurityCode;
        
        username = jTextField1.getText();
        password = jPasswordField1.getText();
        confirm = jPasswordField2.getText();
        securityCode = Integer.parseInt(jTextField2.getText());
        
        if(password.equals(confirm)){
            checkUsername = Register.checkUsename(username);
            checkPassword = Register.checkPassword(password);
            checkSecurityCode = Register.checkSecurityCode(securityCode);
        
            if(checkUsername == false && checkPassword == false && checkSecurityCode == false){
                Facility.registerUser(username, password, securityCode);
                JOptionPane.showMessageDialog(rootPane, "You've been Registered as \""+username+"\".");
                dispose();
                new LogIn().setVisible(true);
            }
            else if(checkUsername == true){
                JOptionPane.showMessageDialog(rootPane, "Username Already Exists, Please try Another Username...");
                jTextField1.setText("");
            }
            else if(checkPassword == true){
                JOptionPane.showMessageDialog(rootPane, "Password Already Exists, Please try Another Password...");
                jPasswordField1.setText("");
                jPasswordField2.setText("");
            }
            else if(checkSecurityCode == true){
                JOptionPane.showMessageDialog(rootPane, "Security Code Already Exists, Please try Another Security Code...");
                jTextField2.setText("");
            }
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Password not Matched, Please Enter Same Password in Both Fields!!!");
            jPasswordField1.setText("");
            jPasswordField2.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

