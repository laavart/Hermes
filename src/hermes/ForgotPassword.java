    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String username, password, confirm;
        
        username = jTextField1.getText();
        password = jPasswordField2.getText();
        confirm = jPasswordField3.getText();
        
        if(password.equals(confirm)){
            Facility.changePassword(username, password);
            JOptionPane.showMessageDialog(rootPane, "Password Updated...");
            dispose();
            new LogIn().setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Password not Matched, Please Enter Same Password in Both Fields!!!");
            jPasswordField2.setText("");
            jPasswordField3.setText("");
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        String username;
        int securityCode;
        boolean checkUsername, checkSecurityCode;
        
        username = jTextField1.getText();
        securityCode = Integer.parseInt(jPasswordField1.getText());
        
        checkUsername = Register.checkUsename(username);
        checkSecurityCode = Validate.matchSecurityCode(username, securityCode);
        
        if(checkUsername==true && checkSecurityCode==true){
            JOptionPane.showMessageDialog(rootPane, "Creditials Matched, Now you can Change your Password!!!");
            jPasswordField2.setEditable(true);
            jPasswordField3.setEditable(true);
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "User not Found, Try Again!!!");
            jTextField1.setText("");
            jPasswordField1.setText("");
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

